# Устройство состоит из трех деталей. Для первой детали вероятность выйти из строя в первый месяц равна 0.1,
# для второй - 0.2, для третьей - 0.25. Какова вероятность того, что в первый месяц выйдут из строя: а). все детали
# б). только две детали в). хотя бы одна деталь г). от одной до двух деталей?


# вероятность выхода всех деталей из строя находим через произведение вероятностей независимых событий

fst_obj, scd_obj, trd_obj = 0.1, 0.2, 0.25
a_situation = fst_obj * scd_obj * trd_obj

# сумма вероятностей всех возможных событий
b_situation = sum(
    [fst_obj * (1 - scd_obj) * trd_obj, (1 - fst_obj) * scd_obj * trd_obj, fst_obj * scd_obj * (1 - trd_obj)])

# Можем вычесть из 1 (общей вероятности) вероятность

c_situation = sum([fst_obj * (1 - scd_obj) * (1 - trd_obj), (1 - fst_obj) * scd_obj * (1 - trd_obj),
                   (1 - fst_obj) * (1-scd_obj) * trd_obj])

# Находим через сумму вероятности выхода из строя 1 детали и 2х деталей
d_situation = c_situation + b_situation
print(f'Вероятность того что все детали выйдут из строя составляет {a_situation}, только 2 детали {b_situation},'
      f'хотябы 1 деталь {c_situation}, от одной до 2 деталей {d_situation}')



# В первом ящике находится 8 мячей, из которых 5 - белые. Во втором ящике - 12 мячей, из которых 5 белых. Из первого
# ящика вытаскивают случайным образом два мяча, из второго - 4. Какова вероятность того, что 3 мяча белые?

# вероятность будет равно сумме вероятностей возможных событий 0 белых и 3 белых, 1 белый и 2 белых, 2 белых и 1 белый

a_chance = (Veroyatnost(0, 5).combinations() / Veroyatnost(2, 8).combinations()) * \
           (Veroyatnost(3, 5).combinations() / Veroyatnost(4, 12).combinations())

b_chance = (Veroyatnost(1, 5).combinations() / Veroyatnost(2, 8).combinations()) * \
           (Veroyatnost(2, 5).combinations() / Veroyatnost(4, 12).combinations())

c_chance = (Veroyatnost(2, 5).combinations() / Veroyatnost(2, 8).combinations()) * \
           (Veroyatnost(1, 5).combinations() / Veroyatnost(4, 12).combinations())

print()
result = sum([a_chance, b_chance, c_chance])
print(f'Вероятность вытащить три белых мяча {result}\n')


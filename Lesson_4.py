print(f'вероятность больше 182 см {my_disrib[my_disrib>182].shape[0]/my_disrib.shape[0]}')
print(f'вероятность больше 190 см {my_disrib[my_disrib>190].shape[0]/my_disrib.shape[0]}')
print(f'вероятность от 166 см до 190 см {my_disrib[(my_disrib>=166)& (my_disrib<=190)].shape[0]/my_disrib.shape[0]}')
print(f'вероятность от 166 см до 182 см {my_disrib[(my_disrib>=166)& (my_disrib<=182)].shape[0]/my_disrib.shape[0]}')

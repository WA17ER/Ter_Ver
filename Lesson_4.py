# О случайной непрерывной равномерно распределенной величине B известно, что ее дисперсия равна 0.2.
# Можно ли найти правую границу величины B и ее среднее значение зная, что левая граница равна 0.5?
# Если да, найдите ее.

# зная формулу для дисперсии ((b-a)**2/12) можем найти а(левую границу) a = b - sqrt(d * 12) и потом вычеслить среднее

b = 0.5
d = 0.2
a =  b - sqrt(d * 12)
mean_val = (a+b/2)
print(f'левая граница равна {a}, среднее равно {mean_val}\n')

